        '<p class="d-text">مجموعة: ' + g + ' | كود: ' + i + '</p></div>' +
        '<div style="font-size:10px; color:#999; margin-top:10px;">معسكر التدريب الطلابي 2026</div></div>' +
        '<button onclick="location.reload()" style="margin-top:20px; background:#334155; color:white; border:none; padding:10px; border-radius:8px; cursor:pointer;">بحث جديد</button>';

