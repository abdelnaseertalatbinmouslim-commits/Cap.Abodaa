function start_search() {
    const val = document.getElementById('search_in').value.trim();
    const res_box = document.getElementById('res_area');
    res_box.innerHTML = "";

    if (val === "") {
        alert("من فضلك اكتب اسم للبحث");
        return;
    }

    let foundCount = 0;
    _data.forEach(function(item) {
        if (item._n.includes(val)) {
            foundCount++;
            // إنشاء Div منفصل لكل نتيجة لضمان نزولها في سطر جديد
            const row = document.createElement('div');
            row.className = 's_row';
            row.style.display = "block"; // تأكيد إضافي عشان ينزل سطر
            row.style.marginBottom = "10px";
            row.innerHTML = '<div style="display:flex; justify-content:space-between; align-items:center; width:100%;">' +
                           '<span>' + item._n + '</span>' + 
                           '<button onclick="get_barcode(\''+item._n+'\', \''+item._id+'\', \''+item._g+'\')">عـرض</button>' +
                           '</div>';
            res_box.appendChild(row);
        }
    });

    if (foundCount === 0) {
        res_box.innerHTML = "<p style='color:#ff4d4d'>الاسم غير موجود</p>";
    }
}

